#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*a;
	int		i;

	a = b;
	i = 0;
	while (len != 0)
	{
		a[i] = c;
		i++;
		len--;
	}
	return (a);
}
